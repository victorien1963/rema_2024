        checkitem = function() {
            $this = $("#product-slide1");
            if ($("#product-slide1 .carousel-inner").children('div').length === 1) {
                $this.children(".carousel-control").hide();
            } else if ($("#product-slide1 .carousel-inner .item:first").hasClass("active")) {
                $this.children(".left").hide();
                $this.children(".right").show();
            } else if ($("#product-slide1 .carousel-inner .item:last").hasClass("active")) {
                $this.children(".right").hide();
                $this.children(".left").show();
            } else {
                $this.children(".carousel-control").show();
            }
        };

        checkitem();
        $this.on("slid.bs.carousel", "", checkitem);
